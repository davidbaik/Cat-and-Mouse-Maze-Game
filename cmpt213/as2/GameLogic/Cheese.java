package ca.cmpt213.as2.GameLogic;

public class Cheese {
	private int xPosition = 0;
	private int yPosition = 0;
	
	public Cheese(int xPosition, int yPosition) {
		this.xPosition = xPosition;
		this.yPosition = yPosition;
	}
	
	public int getXPos() {
		return xPosition;
	}
	
	public int getYPos() {
		return yPosition;
	}
	
}
